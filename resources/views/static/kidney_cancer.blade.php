            <h2>Overview</h2><br>

            Kidney cancer is cancer that begins in your kidneys. Your kidneys are two bean-shaped organs, each about the size of your fist. They're located behind your abdominal organs; one kidney is on either side of your spine.<br><br>

            Renal cell carcinoma is the most common type of kidney cancer in adults. Other, less common types of kidney cancer can also occur. For example, young children are more likely to develop a type of kidney cancer called Wilms tumor.<br><br>

            <h2>Symptoms</h2><br> 
            Kidney cancer doesn't show any signs or symptoms in its early stages. Over time, signs and symptoms may appear and include:<br><br>

            •	Blood in your urine, which may appear pink, red, or cola-colored<br><br>
            •	Back or side pain that doesn't go away<br><br>
            •	Loss of appetite<br><br>
            •	Unexplained weight loss<br><br>
            •	Feeling tired<br><br>
            •	Fever<br><br>

            <h2>Causes</h2><br>
            The cause of most kidney cancers is not clear.<br><br>

            Doctors know that kidney cancer begins when changes (mutations) occur in the DNA of some kidney cells. A cell's DNA contains instructions that tell a cell what to do. These DNA changes tell cells to grow and divide quickly. The accumulating abnormal cells form a tumor that can spread beyond the kidney. Some cells may also divide and travel to other parts of the body.<br><br>

            <h2>Risk factors</h2><br>
            Factors that may increase your risk of kidney cancer include:<br><br>

            •	Aging. Your risk of kidney cancer increases as you get older.<br><br>

            •	Smoking. Smokers are more likely to develop kidney cancer than nonsmokers, and the risk decreases after you quit.<br><br>

            •	Obesity. People who are obese are at a higher risk of kidney cancer than people who are at a healthy weight.<br><br>

            •	High blood pressure (hypertension). High blood pressure increases your risk of kidney cancer.<br><br>

            •	Treatment of kidney failure. People who undergo dialysis (kidney dialysis) for long periods of time to treat chronic kidney failure are at higher risk of kidney cancer.<br><br>

            •	Certain inherited syndromes. People born with certain inherited syndromes may be more likely to develop kidney cancer, such as those with von Hippel-Lindau disease, Birt-Hogg-Dube syndrome, tuberous sclerosis complex, hereditary papillary renal cell carcinoma, or familial kidney cancer.<br><br>

            •	Having a family history of kidney cancer. Your chances of developing kidney cancer increase if a family member has had it.<br><br>

            <h2>Prevention</h2><br>
            Taking steps to improve your health may help reduce your risk of kidney cancer. To reduce your risk, try the following:<br><br>

            •	Quit smoking. If you smoke, quit. There are many options for quitting smoking, including support programs, medications, and nicotine replacement products. Tell your doctor that you want to quit smoking, and discuss your options with him or her.<br><br>

            •	Maintain a healthy weight. Work to maintain a healthy weight. If you are overweight or obese, reduce the number of calories you consume each day and try to be physically active most days of the week. Ask your doctor about other healthy strategies to help you lose weight.<br><br>

            •	Manage high blood pressure. Ask your doctor to check your blood pressure at your next appointment. If your blood pressure is high, you can discuss options for lowering your readings. Lifestyle measures such as exercise, losing weight, and changing your diet can help. Some people may need to add medications to lower their blood pressure. Discuss your options with your doctor.<br><br>

            <h2>When to see a doctor?</h2><br>
            Make an appointment with your doctor if you have any persistent signs or symptoms that worry you.<br><br>


