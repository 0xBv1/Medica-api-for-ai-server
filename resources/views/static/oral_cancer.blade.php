        <h1 class="banner-title">Oral Cancer</h1>
        <img src={{asset('assets/images/article_images/oral_cancer.jpg')}} alt="" style="width: 55%;"><br>
        <p>
            <h1>Overview</h1>
            <p>Mouth cancer is a growth of cells that starts in the mouth. Mouth cancer can happen in any of the parts that make up the mouth. Mouth cancer can occur on the:</p><br>
            <ul>
                <li><strong>• </strong> Lips.</li>
                <li><strong>• </strong> Gums.</li>
                <li><strong>• </strong> Tongue.</li>
                <li><strong>• </strong> Inner lining of the cheeks.</li>
                <li><strong>• </strong> Roof of the mouth.</li>
                <li><strong>• </strong> Floor of the mouth.</li>
            </ul><br>
            <p>The mouth also is called the oral cavity. Cancer that happens in the mouth is sometimes called oral cancer or oral cavity cancer.</p>
            <p>Mouth cancer is one of several cancers that are considered to be types of head and neck cancer. Mouth cancer and other head and neck cancers often have similar treatments.</p>

            <h1>Symptoms</h1>
            <p>Signs and symptoms of mouth cancer may include:</p><br>
            <ul>
                <li><strong>• </strong> A lip or mouth sore that won't heal.</li>
                <li><strong>• </strong> A white or reddish patch on the inside of the mouth.</li>
                <li><strong>• </strong> Loose teeth.</li>
                <li><strong>• </strong> A growth or lump inside the mouth.</li>
                <li><strong>• </strong> Mouth pain.</li>
                <li><strong>• </strong> Ear pain.</li>
                <li><strong>• </strong> Difficult or painful swallowing.</li>
            </ul><br>

            <h2>When to see a doctor</h2>
            <p>Make an appointment with a doctor, dentist or other healthcare professional if you have any symptoms that worry you.</p>

            <h1>Causes</h1>
            <p>It's not always clear what causes mouth cancer. This cancer starts as a growth of cells in the mouth. It most often starts in cells called squamous cells. These are flat, thin cells that line the lips and the inside of the mouth. Most oral cancers are squamous cell carcinomas.</p><br>
            <p>Mouth cancer happens when cells on the lips or in the mouth develop changes in their DNA. A cell's DNA holds the instructions that tell the cell what to do. In healthy cells, the DNA tells the cells to grow and multiply at a set rate. The DNA also tells the cells to die at a set time.</p><br>
            <p>In cancer cells, the DNA changes give different instructions. The changes tell the cancer cells to grow and multiply quickly. Cancer cells can keep living when healthy cells would die. This causes too many cells.</p><br>
            <p>The cancer cells might form a mass called a tumor. The tumor can grow to invade and destroy healthy body tissue. In time, the cancer cells can break away and spread to other parts of the body. When cancer spreads, it's called metastatic cancer.</p>

            <h1>Risk factors</h1>
            <p>Factors that can increase the risk of mouth cancer include:</p><br>
            <ul>
                <li><h3>1. Using tobacco</h3><br> All forms of tobacco increase the risk of mouth cancer. This includes cigarettes, cigars, pipes, chewing tobacco and snuff.</li><br>
                <li><h3>2. Drinking alcohol</h3><br> Frequent and heavy drinking increases the risk of mouth cancer. Using alcohol and tobacco together increases the risk even more.</li><br>
                <li><h3>3. Excessive sun exposure to the lips</h3><br> Ultraviolet light from the sun and tanning lamps increases the risk of lip cancer.</li><br>
                <li><h3>4. Being exposed to human papillomavirus</h3><br> Human papillomavirus (HPV) is a common virus passed through sexual contact. It can lead to many types of cancer, including mouth cancer.</li><br>
                <li><h3>5. A weakened immune system</h3><br> If the immune system is weakened by medicines or illness, such as after an organ transplant or with HIV, the risk of mouth cancer increases.</li><br>
            </ul>

            <h1>Prevention</h1>
            <p>There's no proven way to prevent mouth cancer. However, you may reduce your risk if you:</p><br>
            <ul>
                <li><h3>1. Don't use tobacco</h3><br> If you don't use tobacco, don't start. If you currently use it, talk with your healthcare team about strategies to help you quit.</li><br>
                <li><h3>2. Limit alcohol intake</h3><br> If you choose to drink, do so in moderation. For healthy adults, this means up to one drink a day for women and up to two for men.</li><br>
                <li><h3>3. Avoid excessive sun exposure to your lips</h3><br> Stay in the shade, wear a broad-brimmed hat, and apply a sunscreen lip product for protection.</li><br>
                <li><h3>4. Consider the HPV vaccine</h3><br> Getting vaccinated against HPV may reduce the risk of HPV-related cancers, including mouth cancer.</li><br>
                <li><h3>5. Have regular health and dental exams</h3><br> Your healthcare provider can check your mouth for signs of cancer during routine visits.</li><br>
            </ul>
