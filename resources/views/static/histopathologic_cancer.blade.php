            <h2>Overview</h2><br>
            Soft tissue sarcoma is a rare type of cancer that begins as a growth of cells in the body's soft tissues. Soft tissues connect, support, and surround the body's organs. Soft tissues include muscles, fat, blood vessels, nerves, tendons, and the linings of joints.<br><br>

            Soft tissue sarcomas can occur anywhere in the body. They most often occur in the arms, legs, and abdomen.<br><br>

            There are more than 50 types of soft tissue sarcomas. Some types occur more often in children, and others occur more often in adults. These cancers can be difficult to diagnose because they can be mistaken for other types of tumors.<br><br>

            Soft tissue sarcomas are usually treated with surgery. Other treatments may include radiation therapy and chemotherapy. Treatment depends on the size, type, location, and speed of growth of the cancer.<br><br>

