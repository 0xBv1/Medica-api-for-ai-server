            <h2>Risk factors</h2><br>

            Factors that may increase your risk of developing sarcoma include:<br><br>

            <h3>• Inherited syndromes</h3><br> 
            The risk of developing soft tissue sarcoma can run in families. Genetic syndromes that increase your risk include hereditary retinoblastoma, Li-Fraumeni syndrome, familial adenomatous polyposis, neurofibromatosis, tuberous sclerosis, and Werner syndrome.<br><br>

            <h3>• Exposure to chemicals</h3><br> 
            Exposure to certain chemicals may increase your risk of developing soft tissue sarcoma. These chemicals include herbicides, arsenic, and dioxin.<br><br>

            <h3>• Exposure to radiation</h3><br>
            Radiation therapy for other types of cancer can increase your risk of developing soft tissue sarcoma.<br><br>

