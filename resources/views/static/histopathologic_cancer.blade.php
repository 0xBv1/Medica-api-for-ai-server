            <h2>Symptoms</h2><br>
            A soft tissue sarcoma may not cause any symptoms at first. But as the cancer grows, it may cause:<br><br>

            •	A noticeable lump or swelling<br>
            •	Pain if the growth presses on nerves or muscles<br><br>

            <h2>Causes</h2><br>
            The cause of most types of soft tissue sarcoma is not known.<br><br>

            Soft tissue sarcoma begins when changes occur in the DNA of a connective tissue cell. A cell's DNA carries instructions that tell a cell to do what it does. These changes cause the connective tissue cell to become cancerous. These changes tell the cancer cells to grow and make more cells. Normal cells die as part of their normal cycle, but cancer cells keep growing because they don't have instructions to stop.<br><br>

            The cancer cells accumulate to form a tumor. In some types of soft tissue sarcoma, the cancer cells stay in one place. They continue to make more cells and cause the tumor to grow larger. In other types of soft tissue sarcoma, the cancer cells may break away from the main tumor and spread to other parts of the body.<br><br>

            The type of cell with the DNA changes determines the type of soft tissue sarcoma. For example, angiosarcoma begins in cells in the lining of blood vessels, while liposarcoma begins in fat cells.<br><br>

