            <h2>Overview</h2><br>
            Skin cancer — an abnormal growth of skin cells — usually appears on skin exposed to the sun. But this common type of cancer can also appear on areas of your body that aren’t normally exposed to the sun.<br><br>

            There are three main types of skin cancer: basal cell carcinoma, squamous cell carcinoma, and melanoma.<br><br>

            You can reduce your risk of skin cancer by limiting or avoiding exposure to ultraviolet (UV) light. Checking your skin for suspicious changes can help detect skin cancer early. Early detection of skin cancer gives you the best chance of successfully treating it.<br><br>

            <h2>Types</h2><br>
            •	Melanoma<br>
            •	Squamous cell carcinoma of the skin<br>
            •	Basal cell carcinoma<br>
            •	Non-melanoma skin cancer<br><br>

            <h2>Symptoms Where skin cancer develops </h2><br>

            <h3>• Basal cell carcinoma on fair skin</h3><br>
                                Basal cell carcinoma is a type of skin cancer that most often occurs on areas of skin exposed to the sun, such as the face. Basal cell carcinoma usually appears as a flesh-colored or pink bump.<br><br>

            <h3>• Squamous cell carcinoma of the skin</h3><br>
                                Sun-exposed areas, such as the lips and ears, are particularly susceptible to squamous cell carcinoma of the skin.<br><br>


            <h3>• Melanoma</h3><br>
                                The first sign of melanoma is often a mole that changes in size, shape or color. This melanoma shows color changes and an irregular barrier, both of which are warning signs of melanoma.<br><br>

            <h3>• Merkel cell carcinoma</h3><br>
                                Merkel cell carcinoma is a rare and aggressive type of skin cancer. It appears as a painless, flesh-colored or bluish-red nodule that grows on the skin.<br><br>

            Melanoma develops mainly on areas of skin exposed to the sun, including the scalp, face, lips, ears, neck, chest, arms and hands, and on the legs in women. But it can also form on areas that rarely see the sun — your palms, under your fingernails or toenails, or your genital area.<br><br>

            Skin cancer can affect people of all skin tones, including people with darker skin. When people with darker skin do get skin cancer, it often appears on areas that aren't usually exposed to the sun, such as the palms of your hands and soles of your feet.<br><br>

            <h2>Signs and symptoms of squamous cell carcinoma</h2><br>
            Squamous cell carcinoma usually occurs on areas of your body that are exposed to the sun, such as your neck or face.<br><br>

            <h2>Squamous cell carcinoma may appear as:</h2><br>

            •	A pearly or waxy bump<br>
            •	A flat, skin-colored lesion, or it may appear as a brown scar<br>
            •	A bleeding or crusty sore that heals and then comes back<br><br>

            <h2>Signs and symptoms of squamous cell carcinoma of the skin</h2><br>
            Squamous cell carcinoma of the skin most often occurs on areas of your body that are exposed to the sun, such as your face, ears and hands. People with darker skin are more likely to get squamous cell carcinoma on areas of your body that aren't often exposed to the sun.<br><br>

            Squamous cell carcinoma may appear as:<br><br>

            •	A red, firm nodule<br>
            •	A flat lesion with a scaly, scaly surface<br><br>


            <h2>Signs and symptoms of skin cancer</h2><br>

            Skin cancer can occur anywhere on your body, whether in normal skin or in a mole that becomes cancerous. In men, skin cancer most often appears on the face or trunk. In women, this type of cancer most often appears on the lower legs. In both men and women, skin cancer can occur on skin that has not been exposed to the sun.<br><br>

            Skin cancer can affect people of any skin color. In people with darker skin, skin cancer tends to occur on the palms of the hands, soles of the feet, or under the fingernails or toenails.<br><br>

            <h2>Signs of skin cancer include:</h2><br>

            •	A large brown spot with other dark spots<br><br>
            •	A mole that changes color, size, feel, or bleeds<br><br>
            •	A small lesion with irregular borders and parts that appear red, pink, white, blue, or blue-black<br><br>
            •	A painful lesion that itches or burns<br><br>
            •	Dark lesions on the palms of the hands, soles of the feet, tips of the fingers or toes, or on the mucous membranes lining the mouth, nose, vagina, or anus<br><br>  

            <h2>Signs and symptoms of less common types of skin cancer</h2><br>

            Other less common types of skin cancer include:<br><br>

            •	Kaposi's sarcoma. This rare type of skin cancer develops in the blood vessels of the skin and causes red or purple patches on the skin or mucous membranes.<br><br>

            •	Kaposi's sarcoma mainly affects people with weakened immune systems, such as those with AIDS, and people who take medications that suppress their natural immunity, such as those who have had an organ transplant.<br><br>

            •	Others who are at increased risk of Kaposi's sarcoma include young people who live in Africa or older people of Italian or Eastern European Jewish descent.<br><br>

            •	Merkel cell carcinoma Merkel cell carcinoma causes firm, shiny nodules on or under the skin and in hair follicles. Merkel cell carcinoma most often appears on the head, neck, and trunk.<br><br>

            •	Sebaceous gland carcinoma This uncommon and aggressive type affects the oil glands in the skin. Sebaceous gland cancer, which usually appears as a hard, painless nodule, can appear anywhere, but most occur on the eyelid, which is why many people mistake it for another eyelid condition.<br><br>


            <h2>Where skin cancer develops</h2><br>

            <img src="../images/article_images/skin_cancer_2.png" alt=""><br><br>

            Skin cancer begins in the cells that make up the epidermis, the outer layer of your skin. One type of skin cancer is called basal cell carcinoma, which begins in the basal cells. Basal cells are the cells that push older cells toward the surface of the skin. As new cells move toward the surface, they become squamous cells. Skin cancer that begins in the squamous cells is called squamous cell carcinoma of the skin. Melanoma is another type of skin cancer that begins in pigment cells called melanocytes.<br><br>

            <h2>Causes</h2><br>

            Skin cancer occurs when errors (mutations) occur in the DNA of skin cells. The mutations cause the cells to grow out of control and form a mass of cancerous cells.<br><br>

            <h2>When to see a doctor?</h2><br>

            Make an appointment with your doctor if you have signs or symptoms on your skin that worry you. Note that not all skin changes are caused by skin cancer. Your doctor will check your skin changes to determine the cause.<br><br>


